function buildProductImages(productId: string, primaryUrl: string, secondaryUrl?: string) {
  const images: ProductImage[] = [
    {
      id: `${productId}-image-1`,
      product_id: productId,
      url: primaryUrl,
      alt_text: "Product image",
      sort_order: 0,
    },
  ];

  if (secondaryUrl) {
    images.push({
      id: `${productId}-image-2`,
      product_id: productId,
      url: secondaryUrl,
      alt_text: "Secondary product image",
      sort_order: 1,
    });
  }

  return images;
}

function resolveCategory(slug: string) {
  const category = categoryDefinitions.find((item) => item.slug === slug);

  if (!category) {
    throw new Error(`Unknown category slug: ${slug}`);
  }

  return category;
}

function makeProduct(input: {
  id: string;
  name: string;
  slug: string;
  categorySlug: string;
  sku: string;
  price: number;
  description: string;
  brand: string;
  badge: ProductBadge;
  featured?: boolean;
  collection_slugs: string[];
  rating: number;
  review_count: number;
  attributes: Record<string, string>;
  image: string;
  secondaryImage?: string;
  createdAt: string;
}) {
  const category = resolveCategory(input.categorySlug);
  const productImages = buildProductImages(
    input.id,
    input.image,
    input.secondaryImage,
  );

  return {
    id: input.id,
    name: input.name,
    slug: input.slug,
    description: input.description,
    price: input.price,
    category_id: category.id,
    sku: input.sku,
    gtin: null,
    brand: input.brand,
    attributes: input.attributes,
    meta_title: `${input.name} | DavidEcomm`,
    meta_description: input.description,
    og_image_url: input.image,
    in_stock: true,
    active: true,
    featured: input.featured ?? false,
    badge: input.badge,
    collection_slugs: input.collection_slugs,
    rating: input.rating,
    review_count: input.review_count,
    created_at: input.createdAt,
    updated_at: input.createdAt,
    product_images: productImages,
    categories: category,
  } satisfies Product;
}

